// ─── Media ───────────────────────────────────────────────────────────────────

export interface MediaItem {
  type: 'image' | 'video';
  url: string;
  thumbnail?: string; // for video: poster frame; for image: ignored
}

// ─── Variant ─────────────────────────────────────────────────────────────────

export interface ProductVariant {
  sku: string;
  color: string;
  size: string;
  price: number;
  originalPrice?: number;
  stock: number;
  images: string[];
  enabled: boolean;
}

// ─── Structured tags ─────────────────────────────────────────────────────────

export interface ProductTags {
  material?: string[];
  color?: string[];
  style?: string[];
  room?: string[];
  price_range?: string[];
}

// ─── Category path ────────────────────────────────────────────────────────────

export interface CategoryPath {
  level1: string;   // e.g. "Storage", "Living Room", "Bedroom"
  level2?: string;  // e.g. "TV Stand", "Sofa", "Dining Chair"
}

// ─── Product ─────────────────────────────────────────────────────────────────

export interface Product {
  id: string;
  name: string;
  shortTitle?: string;
  category: string;
  desc: string;
  price: number;
  originalPrice?: number;
  discountPercent: number;
  rating: number;
  reviewCount: number;
  stock: number;
  isNew?: boolean;
  isBestSeller?: boolean;
  isFeatured?: boolean;
  tags?: ProductTags;
  categoryPath?: CategoryPath;  // structured category; backward-compat with category: string
  images: string[];
  image?: string;                // convenience alias for images[0]
  thumbnail?: string;            // alias for images[0], used by some card renderers
  coverImage?: string;           // alias for images[0], used by some card renderers
  product_family_key?: string;   // groups same-style different-color products
  variants?: ProductVariant[];
  bundleIds?: string[];
  createdAt?: string;
  sales?: number;
  commission?: number;
  displayTitle?: string;
  skuCustom?: string;
  skuSearch?: string;
  categoryLabel?: string;      // normalized category: 'Cabinet' | 'Dresser' | 'TV Stand' | … | 'Other'
  categoryPriority?: number;   // UI sort order from pipeline
  isNewArrival?: boolean;      // populated by normalization pipeline
  newArrivalSource?: string;  // 'api' | 'raw' | 'fallback' | 'none'
  newArrivalAddedAt?: string; // ISO timestamp from supplier addedTime
  features?: string[];
  specs?: { group?: string; label: string; value: string }[];
  media?: MediaItem[];
}

// ─── Utilities ────────────────────────────────────────────────────────────────

export function formatPrice(n: number): string {
  return n.toLocaleString('en-US', { minimumFractionDigits: 0, maximumFractionDigits: 0 });
}

export function recommendationScore(p: Product): number {
  return (
    (p.isBestSeller ? 5 : 0) +
    (p.isNew ? 3 : 0) +
    (p.rating >= 4.5 ? 3 : 0) +
    (p.discountPercent > 0 ? 2 : 0) +
    (p.stock > 0 ? 2 : 0) +
    (p.isFeatured ? 2 : 0)
  );
}

